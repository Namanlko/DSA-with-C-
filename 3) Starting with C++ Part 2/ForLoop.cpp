// For Loop: For loop is an entry-controlled loop that is used to execute a block of code repeatedly for the specified range of values. Basically, for loop allows you to repeat a set of instructions for a specific number of iterations.

#include<iostream>
using namespace std;

int main(){
    
    // Example of for loop:
    for(int i=1; i<=10; i++){
        cout<<i<<" ";
    }

    return 0;
}