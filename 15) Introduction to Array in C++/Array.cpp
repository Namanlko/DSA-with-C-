// Array: An array is a linear data structure where all the elements are arranged sequentially. It is a collection of elements of same data type stored at contiguous memeory location.

#include<iostream>
using namespace std;

int main(){
    
    // Array Declaration & Initialization:
    int arr[] = {1,2,3,4,5};

    // Access Array element with the help of index:
    cout<<arr[0]<<endl;
    cout<<arr[1]<<endl;
    cout<<arr[2]<<endl;
    cout<<arr[3]<<endl;
    cout<<arr[4]<<endl;

    return 0;
}